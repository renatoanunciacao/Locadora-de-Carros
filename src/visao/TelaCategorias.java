    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        CadastroCategorias tela = new CadastroCategorias(this, rootPaneCheckingEnabled);
        tela.setVisible(true);
        
        Categoria novo = tela.getCategoria();
        
        if(novo != null){
            CategoriasDAO dao = new CategoriasDAO();
            dao.inserir(novo);
            carregarCategorias();
        }


    }//GEN-LAST:event_btnaddActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        try {
            int indiceLinha = tblCategorias.getSelectedRow();
            
            Categoria selecionado = (Categoria)modeloTabela.getValueAt(indiceLinha, 3);
            
            CadastroCategorias tela = new CadastroCategorias(this, rootPaneCheckingEnabled);
            tela.setCategoria(selecionado);
            tela.setVisible(true);
            
            if(selecionado != null){
                CategoriasDAO dao = new CategoriasDAO();
                dao.editar(selecionado);
                carregarCategorias();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane,"Selecione uma Categoria " +e.getMessage());
        }
    }//GEN-LAST:event_btneditActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        try {
            int indiceLinha = tblCategorias.getSelectedRow();

            Categoria selecionado = (Categoria) modeloTabela.getValueAt(indiceLinha, 3);

            if (selecionado != null) {
                int op = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir a categoria selecionada?");
                if (op == JOptionPane.YES_NO_OPTION) {

                    CategoriasDAO dao = new CategoriasDAO();
                    dao.remover(selecionado);

                }else{
                    JOptionPane.showMessageDialog(rootPane, "Escolheu n√£o excluir  a categoria");
                }
                carregarCategorias();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Selecione uma categoria "+ e.getMessage());
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void txtCategoriaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCategoriaKeyReleased
        CategoriasDAO dao = new CategoriasDAO();
        String nome= txtCategoria.getText().trim().toLowerCase();
        modeloTabela.setNumRows(0);
        for(Categoria c: dao.buscarcategorias(nome)){
            modeloTabela.addRow(new Object[]{c.getId(), c.getNome_cat(),c.getValor(), c});

        }
    }//GEN-LAST:event_txtCategoriaKeyReleased

