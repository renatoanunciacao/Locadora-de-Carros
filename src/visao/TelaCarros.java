    public void carregarCarros() {
        modeloTabela.setNumRows(0);

        CarroDAO dao = new CarroDAO();
        for (Carro c : dao.buscarcarros()) {
            modeloTabela.addRow(new Object[]{c.getId(), c.getNome(), c.getPlaca(), c.getMarca(), c.getChasi(), sdf.format(c.getAno()), c.getCategoria().getNome_cat(), c});

        }

    }

    public void carregarCategorias() {
        cmbCategorias.removeAllItems();
        CategoriasDAO dao = new CategoriasDAO();
        cmbCategorias.addItem(null);
        for (Categoria u : dao.buscarcategorias()) {
            cmbCategorias.addItem(u);
        }
    }

