    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        CadastrarCarros tela = new CadastrarCarros(this, rootPaneCheckingEnabled);
        tela.setVisible(true);

        Carro novo = tela.getCarro();

        if (novo != null) {
            CarroDAO dao = new CarroDAO();
            dao.inserir(novo);
            carregarCarros();
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        try {
            int indiceLinha = tblCarros.getSelectedRow();

            Carro selecionado = (Carro) modeloTabela.getValueAt(indiceLinha, 7);
            if (selecionado != null) {
                int op = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o carro selecionada?");
                if (op == JOptionPane.YES_NO_OPTION) {

                    CarroDAO dao = new CarroDAO();
                    dao.remover(selecionado);

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Escolheu n√£o excluir o carro");
                }
                carregarCarros();

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um carro para remover " + e.getMessage());
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        int indiceLinha = tblCarros.getSelectedRow();
        if (indiceLinha != -1) {

            Carro selecionado = (Carro) modeloTabela.getValueAt(indiceLinha, 7);

            CadastrarCarros tela = new CadastrarCarros(this, rootPaneCheckingEnabled);
            tela.setCarro(selecionado);
            tela.setVisible(true);

            if (selecionado != null) {
                CarroDAO dao = new CarroDAO();
                dao.editar(selecionado);
                carregarCarros();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Carro ");
        }

    }//GEN-LAST:event_btnEditarActionPerformed

